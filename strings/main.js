// Strings can be defined with single or double quotes
let a = 'Hello World!';
let b = "Goodbye World!";

/* Template strings allow for multiline strings and allow for variables
   to be inserted into a string */
let c = 'World!'
let d = `This is a
multiline string.
Hello ${c}.`

// Print strings to console
console.log(a);
console.log(b);
console.log(d);