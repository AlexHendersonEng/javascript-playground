/* 
    Equality operator (==): Compare if values are equal.
    Strict equality operator (===): Compare if values and datatype are equal.
    Inequality operator (!=): Compare if values are not equal.
    Strict inequality operator (!==): Compare if values or datatype are not equal.
*/

// Define variable for comparisons
const PI = 3.14;

// Use equality operator
(PI == "3.14") ? console.log("Equal") : console.log("Not equal");

// Use strict equality operator
(PI === "3.14") ? console.log("Equal") : console.log("Not equal");